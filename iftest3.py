x = int(input("당신의 파이선 점수를 입력하세요"))

if x>=90 :
      print("당신의 파이선 점수는",x,"점입니다. A학점입니다.")
elif x>=80:
      print("당신의 파이선 점수는",x,"점입니다. B학점입니다.")
elif x>=70:
      print("당신의 파이선 점수는",x,"점입니다. C학점입니다.")
elif x>=60:
      print("당신의 파이선 점수는",x,"점입니다. D학점입니다.")
else:
      print("당신의 파이선 점수는",x,"점입니다. F학점입니다.")




